<?php

/**
 * Estrutura de Controle múltipla.
 * 
 * Usada pra comparar uma variável com vários valores.
 */


 $opcao = 6;


switch ($opcao) {
    case 1:
        echo "Opção 1!\n";
        break;
    case 2:
        echo "Opção 2!\n";
        break;
    case 3:
        echo "Opção 3!\n";
        break;
    default:
        echo "Opção inválida!\n";
        break;
}
